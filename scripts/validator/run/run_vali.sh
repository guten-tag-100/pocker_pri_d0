#!/bin/bash

set -euo pipefail

# Poker44 Validator Startup Script

NETUID="${NETUID:-126}"
WALLET_NAME="${WALLET_NAME:-poker44-test-ck}"
HOTKEY="${HOTKEY:-poker44-hk}"
NETWORK="${NETWORK:-finney}"
SUBTENSOR_PARAM="${SUBTENSOR_PARAM:-}"
VALIDATOR_SCRIPT="${VALIDATOR_SCRIPT:-./neurons/validator.py}"
PM2_NAME="${PM2_NAME:-poker44_validator}"  ##  name of validator, as you wish
VALIDATOR_ENV_DIR="${VALIDATOR_ENV_DIR:-validator_env}"
WALLET_PATH="${WALLET_PATH:-}"
VALIDATOR_EXTRA_ARGS="${VALIDATOR_EXTRA_ARGS:-}"
POKER44_HUMAN_JSON_PATH="${POKER44_HUMAN_JSON_PATH:-/path/to/private/poker_data_combined.json}"
POKER44_RUNTIME_MODE="${POKER44_RUNTIME_MODE:-mixed_dataset}"
POKER44_CHUNK_COUNT="${POKER44_CHUNK_COUNT:-40}"
POKER44_REWARD_WINDOW="${POKER44_REWARD_WINDOW:-40}"
POKER44_POLL_INTERVAL_SECONDS="${POKER44_POLL_INTERVAL_SECONDS:-300}"
POKER44_MINERS_PER_CYCLE="${POKER44_MINERS_PER_CYCLE:-16}"
NEURON_TIMEOUT="${NEURON_TIMEOUT:-60}"
POKER44_PROVIDER_API_BASE_URL="${POKER44_PROVIDER_API_BASE_URL:-http://127.0.0.1:4001}"
POKER44_PROVIDER_INTERNAL_SECRET="${POKER44_PROVIDER_INTERNAL_SECRET:-force-start-secret}"
POKER44_PROVIDER_MIN_EVAL_HANDS="${POKER44_PROVIDER_MIN_EVAL_HANDS:-70}"
POKER44_PROVIDER_MAX_EVAL_HANDS="${POKER44_PROVIDER_MAX_EVAL_HANDS:-120}"
POKER44_PROVIDER_ATTEMPT_PUBLISH_CURRENT="${POKER44_PROVIDER_ATTEMPT_PUBLISH_CURRENT:-true}"
POKER44_PROVIDER_BOOTSTRAP_CMD="${POKER44_PROVIDER_BOOTSTRAP_CMD:-}"
POKER44_PROVIDER_BOOTSTRAP_CWD="${POKER44_PROVIDER_BOOTSTRAP_CWD:-}"
POKER44_PROVIDER_RUNTIME_ROOT="${POKER44_PROVIDER_RUNTIME_ROOT:-$(pwd)/.poker44-provider-runtime}"
POKER44_PROVIDER_BACKEND_DIR="${POKER44_PROVIDER_BACKEND_DIR:-}"
POKER44_PROVIDER_FRONTEND_DIR="${POKER44_PROVIDER_FRONTEND_DIR:-}"
POKER44_PROVIDER_BACKEND_PM2_NAME="${POKER44_PROVIDER_BACKEND_PM2_NAME:-p44_provider_backend}"
POKER44_PROVIDER_FRONTEND_PM2_NAME="${POKER44_PROVIDER_FRONTEND_PM2_NAME:-p44_provider_frontend}"
POKER44_PROVIDER_BACKEND_DOCKER_UP="${POKER44_PROVIDER_BACKEND_DOCKER_UP:-true}"
POKER44_PROVIDER_RUN_MIGRATIONS="${POKER44_PROVIDER_RUN_MIGRATIONS:-true}"
POKER44_PROVIDER_SYNC_NEXT_STATIC="${POKER44_PROVIDER_SYNC_NEXT_STATIC:-false}"
POKER44_PROVIDER_NEXT_STATIC_TARGET="${POKER44_PROVIDER_NEXT_STATIC_TARGET:-}"
POKER44_PROVIDER_SKIP_FRONTEND="${POKER44_PROVIDER_SKIP_FRONTEND:-false}"
POKER44_PROVIDER_BACKEND_REPO_URL="${POKER44_PROVIDER_BACKEND_REPO_URL:-https://github.com/Poker44/poker44-platform-backend.git}"
POKER44_PROVIDER_FRONTEND_REPO_URL="${POKER44_PROVIDER_FRONTEND_REPO_URL:-https://github.com/Poker44/poker44-platform-frontend.git}"
POKER44_PROVIDER_RUNTIME_BRANCH="${POKER44_PROVIDER_RUNTIME_BRANCH:-dev}"
POKER44_PROVIDER_GIT_PULL="${POKER44_PROVIDER_GIT_PULL:-true}"
POKER44_PROVIDER_PUBLIC_HOST="${POKER44_PROVIDER_PUBLIC_HOST:-}"
POKER44_PROVIDER_PUBLIC_BASE_URL="${POKER44_PROVIDER_PUBLIC_BASE_URL:-}"
POKER44_PROVIDER_PUBLIC_API_BASE_URL="${POKER44_PROVIDER_PUBLIC_API_BASE_URL:-}"
POKER44_PROVIDER_FRONTEND_PORT="${POKER44_PROVIDER_FRONTEND_PORT:-4000}"
POKER44_PROVIDER_BACKEND_PORT="${POKER44_PROVIDER_BACKEND_PORT:-4001}"
POKER44_PROVIDER_DATABASE_URL="${POKER44_PROVIDER_DATABASE_URL:-postgresql://aceguard:aceguard_local_pwd@localhost:5433/aceguard_poker}"
POKER44_PROVIDER_REDIS_URL="${POKER44_PROVIDER_REDIS_URL:-redis://localhost:6379}"
POKER44_PROVIDER_JWT_SECRET="${POKER44_PROVIDER_JWT_SECRET:-}"
POKER44_PROVIDER_SHARED_JWT_SECRET="${POKER44_PROVIDER_SHARED_JWT_SECRET:-}"
POKER44_PROVIDER_COOKIE_DOMAIN="${POKER44_PROVIDER_COOKIE_DOMAIN:-}"
POKER44_PROVIDER_CENTRAL_AUTH_ORIGIN="${POKER44_PROVIDER_CENTRAL_AUTH_ORIGIN:-https://dev.poker44.net}"
POKER44_PROVIDER_EXTRA_CORS_ORIGINS="${POKER44_PROVIDER_EXTRA_CORS_ORIGINS:-}"
POKER44_PROVIDER_UFW_MANAGE="${POKER44_PROVIDER_UFW_MANAGE:-true}"
POKER44_PROVIDER_ALLOW_INSECURE_PUBLIC_BASE_URL="${POKER44_PROVIDER_ALLOW_INSECURE_PUBLIC_BASE_URL:-false}"
POKER44_PROVIDER_FIXED_ROOM_CODE="${POKER44_PROVIDER_FIXED_ROOM_CODE:-}"
POKER44_EVAL_COORDINATOR_BASE_URL="${POKER44_EVAL_COORDINATOR_BASE_URL:-http://185.196.20.208:4010}"

if [ -x "$VALIDATOR_ENV_DIR/bin/python" ]; then
    PYTHON_BIN="$VALIDATOR_ENV_DIR/bin/python"
elif command -v python3 >/dev/null 2>&1; then
    PYTHON_BIN="$(command -v python3)"
elif command -v python >/dev/null 2>&1; then
    PYTHON_BIN="$(command -v python)"
else
    echo "Error: No Python interpreter found"
    exit 1
fi

if [ ! -f "$VALIDATOR_SCRIPT" ]; then
    echo "Error: Validator script not found at $VALIDATOR_SCRIPT"
    exit 1
fi

if [ "$POKER44_RUNTIME_MODE" = "mixed_dataset" ] && [ ! -f "$POKER44_HUMAN_JSON_PATH" ]; then
    echo "Error: Private validator human dataset not found at $POKER44_HUMAN_JSON_PATH"
    echo "Set POKER44_HUMAN_JSON_PATH in scripts/validator/run/run_vali.sh before starting."
    exit 1
fi

if [ "$POKER44_RUNTIME_MODE" = "provider_runtime" ] && [ -z "$POKER44_PROVIDER_INTERNAL_SECRET" ]; then
    echo "Error: POKER44_PROVIDER_INTERNAL_SECRET is required in provider_runtime mode."
    exit 1
fi

if [ "$POKER44_RUNTIME_MODE" = "provider_runtime" ] && [ -z "$POKER44_PROVIDER_BOOTSTRAP_CMD" ] && [ -n "$POKER44_PROVIDER_BACKEND_DIR" ]; then
    POKER44_PROVIDER_BOOTSTRAP_CMD="$(pwd)/scripts/validator/provider/start_provider_runtime.sh"
fi

if [ "$POKER44_RUNTIME_MODE" = "provider_runtime" ] && [ -z "$POKER44_PROVIDER_BACKEND_DIR" ]; then
    POKER44_PROVIDER_BACKEND_DIR="$POKER44_PROVIDER_RUNTIME_ROOT/backend"
fi

if [ "$POKER44_RUNTIME_MODE" = "provider_runtime" ] && [ -z "$POKER44_PROVIDER_FRONTEND_DIR" ]; then
    POKER44_PROVIDER_FRONTEND_DIR="$POKER44_PROVIDER_RUNTIME_ROOT/frontend"
fi

if [ "$POKER44_RUNTIME_MODE" = "provider_runtime" ] && [ -z "$POKER44_PROVIDER_BOOTSTRAP_CMD" ]; then
    POKER44_PROVIDER_BOOTSTRAP_CMD="$(pwd)/scripts/validator/provider/start_provider_runtime.sh"
fi

if ! command -v pm2 &> /dev/null; then
    echo "Error: PM2 is not installed"
    exit 1
fi

if ! "$PYTHON_BIN" -c "import bittensor, dotenv, numpy, pandas, sklearn" >/dev/null 2>&1; then
    echo "Error: Python environment is missing required packages for validator startup."
    echo "Checked interpreter: $PYTHON_BIN"
    echo "Run ./scripts/validator/main/setup.sh or fix the virtualenv before starting PM2."
    exit 1
fi

pm2 delete $PM2_NAME 2>/dev/null || true

export PYTHONPATH="$(pwd)"
export POKER44_RUNTIME_MODE="$POKER44_RUNTIME_MODE"
export POKER44_HUMAN_JSON_PATH="$POKER44_HUMAN_JSON_PATH"
export POKER44_CHUNK_COUNT="$POKER44_CHUNK_COUNT"
export POKER44_REWARD_WINDOW="$POKER44_REWARD_WINDOW"
export POKER44_POLL_INTERVAL_SECONDS="$POKER44_POLL_INTERVAL_SECONDS"
export POKER44_MINERS_PER_CYCLE="$POKER44_MINERS_PER_CYCLE"
export POKER44_PROVIDER_API_BASE_URL="$POKER44_PROVIDER_API_BASE_URL"
export POKER44_PROVIDER_INTERNAL_SECRET="$POKER44_PROVIDER_INTERNAL_SECRET"
export POKER44_PROVIDER_MIN_EVAL_HANDS="$POKER44_PROVIDER_MIN_EVAL_HANDS"
export POKER44_PROVIDER_MAX_EVAL_HANDS="$POKER44_PROVIDER_MAX_EVAL_HANDS"
export POKER44_PROVIDER_ATTEMPT_PUBLISH_CURRENT="$POKER44_PROVIDER_ATTEMPT_PUBLISH_CURRENT"
export POKER44_PROVIDER_BOOTSTRAP_CMD="$POKER44_PROVIDER_BOOTSTRAP_CMD"
export POKER44_PROVIDER_BOOTSTRAP_CWD="$POKER44_PROVIDER_BOOTSTRAP_CWD"
export POKER44_PROVIDER_RUNTIME_ROOT="$POKER44_PROVIDER_RUNTIME_ROOT"
export POKER44_PROVIDER_BACKEND_DIR="$POKER44_PROVIDER_BACKEND_DIR"
export POKER44_PROVIDER_FRONTEND_DIR="$POKER44_PROVIDER_FRONTEND_DIR"
export POKER44_PROVIDER_BACKEND_PM2_NAME="$POKER44_PROVIDER_BACKEND_PM2_NAME"
export POKER44_PROVIDER_FRONTEND_PM2_NAME="$POKER44_PROVIDER_FRONTEND_PM2_NAME"
export POKER44_PROVIDER_BACKEND_DOCKER_UP="$POKER44_PROVIDER_BACKEND_DOCKER_UP"
export POKER44_PROVIDER_RUN_MIGRATIONS="$POKER44_PROVIDER_RUN_MIGRATIONS"
export POKER44_PROVIDER_SYNC_NEXT_STATIC="$POKER44_PROVIDER_SYNC_NEXT_STATIC"
export POKER44_PROVIDER_NEXT_STATIC_TARGET="$POKER44_PROVIDER_NEXT_STATIC_TARGET"
export POKER44_PROVIDER_SKIP_FRONTEND="$POKER44_PROVIDER_SKIP_FRONTEND"
export POKER44_PROVIDER_BACKEND_REPO_URL="$POKER44_PROVIDER_BACKEND_REPO_URL"
export POKER44_PROVIDER_FRONTEND_REPO_URL="$POKER44_PROVIDER_FRONTEND_REPO_URL"
export POKER44_PROVIDER_RUNTIME_BRANCH="$POKER44_PROVIDER_RUNTIME_BRANCH"
export POKER44_PROVIDER_GIT_PULL="$POKER44_PROVIDER_GIT_PULL"
export POKER44_PROVIDER_PUBLIC_HOST="$POKER44_PROVIDER_PUBLIC_HOST"
export POKER44_PROVIDER_PUBLIC_BASE_URL="$POKER44_PROVIDER_PUBLIC_BASE_URL"
export POKER44_PROVIDER_PUBLIC_API_BASE_URL="$POKER44_PROVIDER_PUBLIC_API_BASE_URL"
export POKER44_PROVIDER_FRONTEND_PORT="$POKER44_PROVIDER_FRONTEND_PORT"
export POKER44_PROVIDER_BACKEND_PORT="$POKER44_PROVIDER_BACKEND_PORT"
export POKER44_PROVIDER_DATABASE_URL="$POKER44_PROVIDER_DATABASE_URL"
export POKER44_PROVIDER_REDIS_URL="$POKER44_PROVIDER_REDIS_URL"
export POKER44_PROVIDER_JWT_SECRET="$POKER44_PROVIDER_JWT_SECRET"
export POKER44_PROVIDER_SHARED_JWT_SECRET="$POKER44_PROVIDER_SHARED_JWT_SECRET"
export POKER44_PROVIDER_COOKIE_DOMAIN="$POKER44_PROVIDER_COOKIE_DOMAIN"
export POKER44_PROVIDER_CENTRAL_AUTH_ORIGIN="$POKER44_PROVIDER_CENTRAL_AUTH_ORIGIN"
export POKER44_PROVIDER_EXTRA_CORS_ORIGINS="$POKER44_PROVIDER_EXTRA_CORS_ORIGINS"
export POKER44_PROVIDER_UFW_MANAGE="$POKER44_PROVIDER_UFW_MANAGE"
export POKER44_PROVIDER_ALLOW_INSECURE_PUBLIC_BASE_URL="$POKER44_PROVIDER_ALLOW_INSECURE_PUBLIC_BASE_URL"
export POKER44_PROVIDER_FIXED_ROOM_CODE="$POKER44_PROVIDER_FIXED_ROOM_CODE"
export POKER44_EVAL_COORDINATOR_BASE_URL="$POKER44_EVAL_COORDINATOR_BASE_URL"
export PM2_NAME="$PM2_NAME"
export VALIDATOR_ENV_DIR="$VALIDATOR_ENV_DIR"

if [ -n "$SUBTENSOR_PARAM" ]; then
  read -r -a SUBTENSOR_ARG_ARRAY <<< "$SUBTENSOR_PARAM"
else
  SUBTENSOR_ARG_ARRAY=(--subtensor.network "$NETWORK")
fi

VALIDATOR_ARGS=(
  "$VALIDATOR_SCRIPT"
  --netuid "$NETUID"
  --wallet.name "$WALLET_NAME"
  --wallet.hotkey "$HOTKEY"
  --neuron.timeout "$NEURON_TIMEOUT"
  --logging.debug
)

VALIDATOR_ARGS+=("${SUBTENSOR_ARG_ARRAY[@]}")

if [ -n "$WALLET_PATH" ]; then
  VALIDATOR_ARGS+=(--wallet.path "$WALLET_PATH")
fi

if [ -n "$VALIDATOR_EXTRA_ARGS" ]; then
  read -r -a EXTRA_ARG_ARRAY <<< "$VALIDATOR_EXTRA_ARGS"
  VALIDATOR_ARGS+=("${EXTRA_ARG_ARRAY[@]}")
fi

pm2 start "$PYTHON_BIN" \
  --name $PM2_NAME -- \
  "${VALIDATOR_ARGS[@]}"

pm2 save

echo "Validator started: $PM2_NAME"
echo "View logs: pm2 logs $PM2_NAME"
echo "Config: netuid=$NETUID network=$NETWORK wallet=$WALLET_NAME hotkey=$HOTKEY python=$PYTHON_BIN"
echo "Subtensor args: ${SUBTENSOR_PARAM:---subtensor.network $NETWORK}"
echo "Runtime extras: wallet_path=${WALLET_PATH:-<default>} extra_args=${VALIDATOR_EXTRA_ARGS:-<none>}"
echo "Profile: runtime_mode=$POKER44_RUNTIME_MODE chunks=$POKER44_CHUNK_COUNT reward_window=$POKER44_REWARD_WINDOW poll_interval_s=$POKER44_POLL_INTERVAL_SECONDS miners_per_cycle=$POKER44_MINERS_PER_CYCLE timeout_s=$NEURON_TIMEOUT"
if [ "$POKER44_RUNTIME_MODE" = "provider_runtime" ]; then
  echo "Provider runtime: api=$POKER44_PROVIDER_API_BASE_URL min_eval_hands=$POKER44_PROVIDER_MIN_EVAL_HANDS max_eval_hands=$POKER44_PROVIDER_MAX_EVAL_HANDS attempt_publish_current=$POKER44_PROVIDER_ATTEMPT_PUBLISH_CURRENT bootstrap_cmd=${POKER44_PROVIDER_BOOTSTRAP_CMD:-<none>}"
  echo "Provider dirs: backend=${POKER44_PROVIDER_BACKEND_DIR:-<unset>} frontend=${POKER44_PROVIDER_FRONTEND_DIR:-<unset>}"
  echo "Provider repos: backend_repo=$POKER44_PROVIDER_BACKEND_REPO_URL frontend_repo=$POKER44_PROVIDER_FRONTEND_REPO_URL branch=$POKER44_PROVIDER_RUNTIME_BRANCH runtime_root=$POKER44_PROVIDER_RUNTIME_ROOT"
  echo "Provider network: public_base=${POKER44_PROVIDER_PUBLIC_BASE_URL:-<required https host>} public_api_base=${POKER44_PROVIDER_PUBLIC_API_BASE_URL:-<auto>} public_host=${POKER44_PROVIDER_PUBLIC_HOST:-<auto>} backend_port=$POKER44_PROVIDER_BACKEND_PORT frontend_port=$POKER44_PROVIDER_FRONTEND_PORT coordinator=$POKER44_EVAL_COORDINATOR_BASE_URL"
fi
